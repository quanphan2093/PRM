package com.example.myapplication.Slot4;

public class Slot41SinhVien {
    private String name;
    private String age;
    private int pic;

    public Slot41SinhVien() {
    }

    public Slot41SinhVien(String name, String age, int pic) {
        this.name = name;
        this.age = age;
        this.pic = pic;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public int getPic() {
        return pic;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }
}
